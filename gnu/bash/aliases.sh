# aliases.sh: interactive shell aliases

# aliases which let ls and grep employ colored hiliting.
alias ls='ls --color=auto'
alias grep='grep --color=auto'

# some special aliases for coreutils ls
alias ll='ls --color=auto -hal'
alias l='ls --color=auto -l'
alias la='ls --color=auto -la'
alias lsd='ls --color=auto -d'
